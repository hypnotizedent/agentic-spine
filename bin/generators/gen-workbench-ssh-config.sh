#!/usr/bin/env bash
set -euo pipefail

ROOT="${SPINE_ROOT:-$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)}"
BINDING="${ROOT}/ops/bindings/ssh.targets.yaml"
DEFAULT_OUTPUT="/Users/ronnyworks/code/workbench/dotfiles/ssh/config.d/tailscale.conf"

fail() {
  echo "gen.workbench.ssh FAIL: $*" >&2
  exit 1
}

usage() {
  cat <<'EOF'
gen-workbench-ssh-config.sh

Usage:
  gen-workbench-ssh-config.sh
  gen-workbench-ssh-config.sh --write
  gen-workbench-ssh-config.sh --write --output <path>
  gen-workbench-ssh-config.sh --check
  gen-workbench-ssh-config.sh --check --output <path>

Notes:
  - Source of truth is ops/bindings/ssh.targets.yaml.
  - lan_first targets render as Tailscale-first for location-agnostic human SSH.
  - lan_only targets render with ProxyJump via probe_via when declared.
EOF
}

need_cmd() {
  command -v "$1" >/dev/null 2>&1 || fail "missing dependency: $1"
}

render_extra_opts() {
  local extra_opts="$1"
  [[ -n "$extra_opts" && "$extra_opts" != "null" ]] || return 0

  while IFS= read -r opt; do
    [[ -n "$opt" ]] || continue
    opt="${opt#-o }"
    if [[ "$opt" == *=* ]]; then
      printf '    %s %s\n' "${opt%%=*}" "${opt#*=}"
    else
      printf '    # Unparsed SSH option from binding: %s\n' "$opt"
    fi
  done < <(printf '%s\n' "$extra_opts" | sed -E 's/[[:space:]]+-o /\n-o /g')
}

render_targets() {
  local selector="$1"
  yq -r "${selector} | [(.id // \"\"), ((.aliases // []) | join(\" \")), (.host // \"\"), (.tailscale_ip // \"\"), (.user // \"\"), (.access_policy // \"lan_first\"), (.probe_via // \"\"), (.ssh_extra_opts // \"\")] | join(\"|\")" "$BINDING" |
  while IFS='|' read -r id aliases host tailscale_ip user access_policy probe_via extra_opts; do
    [[ -n "$id" ]] || continue

    local host_aliases="$id"
    local rendered_host="$host"

    if [[ -n "$aliases" ]]; then
      host_aliases+=" $aliases"
    fi

    case "$access_policy" in
      lan_first|tailscale_required)
        if [[ -n "$tailscale_ip" && "$tailscale_ip" != "null" ]]; then
          rendered_host="$tailscale_ip"
        fi
        ;;
    esac

    [[ -n "$rendered_host" && "$rendered_host" != "null" ]] || rendered_host="$host"
    [[ -n "$rendered_host" && "$rendered_host" != "null" ]] || fail "target '$id' has no renderable host"

    printf 'Host %s\n' "$host_aliases"
    printf '    HostName %s\n' "$rendered_host"
    printf '    User %s\n' "${user:-root}"
    if [[ "$access_policy" == "lan_only" && -n "$probe_via" && "$probe_via" != "null" ]]; then
      printf '    ProxyJump %s\n' "$probe_via"
    fi
    printf '    IdentityFile ~/.ssh/id_ed25519\n'
    render_extra_opts "$extra_opts"
    printf '\n'
  done
}

mode="stdout"
output="$DEFAULT_OUTPUT"

while [[ $# -gt 0 ]]; do
  case "$1" in
    --write)
      mode="write"
      shift
      ;;
    --check)
      mode="check"
      shift
      ;;
    --output)
      [[ $# -ge 2 ]] || fail "--output requires a path"
      output="$2"
      shift 2
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      fail "unknown argument: $1"
      ;;
  esac
done

[[ -f "$BINDING" ]] || fail "missing binding: $BINDING"
need_cmd yq
need_cmd diff
need_cmd cmp

tmp="$(mktemp)"
trap 'rm -f "$tmp"' EXIT

{
  cat <<'EOF'
# SSH Host Projection (generated)
# Source of truth: ~/code/agentic-spine/ops/bindings/ssh.targets.yaml
# Renderer: ~/code/agentic-spine/bin/generators/gen-workbench-ssh-config.sh
# Do not edit this file manually.
#
# Projection rules:
# - lan_first targets use Tailscale IPs for location-agnostic human SSH.
# - tailscale_required targets use their Tailscale IPs directly.
# - lan_only targets stay on LAN IPs and ProxyJump via probe_via when declared.

# Tailscale-first host inventory
EOF
  render_targets '.ssh.targets[] | select((.access_policy // "lan_first") != "lan_only")'
  cat <<'EOF'
# LAN-only targets routed via declared jump hosts
EOF
  render_targets '.ssh.targets[] | select((.access_policy // "lan_first") == "lan_only")'
} > "$tmp"

case "$mode" in
  stdout)
    cat "$tmp"
    ;;
  write)
    mkdir -p "$(dirname "$output")"
    cp "$tmp" "$output"
    echo "gen.workbench.ssh OK: wrote $output"
    ;;
  check)
    [[ -f "$output" ]] || fail "missing output path for --check: $output"
    if cmp -s "$tmp" "$output"; then
      echo "gen.workbench.ssh OK: $output matches $BINDING"
    else
      echo "gen.workbench.ssh FAIL: $output is out of date with $BINDING" >&2
      diff -u "$output" "$tmp" || true
      exit 1
    fi
    ;;
  *)
    fail "unsupported mode: $mode"
    ;;
esac
